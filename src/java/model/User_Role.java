class User_Role implements Serializable {

    private int userID;
    private int roleID;

    public User_Role() {
    }

    public User_Role(int userID, int roleID) {
        this.userID = userID;
        this.roleID = roleID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public int getRoleID() {
        return roleID;
    }

    public void setRoleID(int roleID) {
        this.roleID = roleID;
    }
}
